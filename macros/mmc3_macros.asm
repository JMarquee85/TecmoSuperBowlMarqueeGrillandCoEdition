
_F{_MMC3_MACROS


.MACRO MMC3_SRAM_WRITE_DISABLE
	LDA #$C0
	STA $A001
.ENDM


.MACRO MMC3_SRAM_WRITE_ENABLE
	LDA #$80
	STA $A001
.ENDM

.MACRO MMC3_SRAM_CHIP_DISABLE
	LDA #$40
	STA $A001
.ENDM

.MACRO MMC3_SET_VERT_MIRRORING
	LDA #$00
	STA $A000
.ENDM

.MACRO MMC3_SET_HORIZ_MIRRORING
	LDA #$01
	STA $A000
.ENDM

.MACRO MMC3_ACK_INTERRUPT
	STA $E000
.ENDM

.MACRO MMC3_IRQ_DISABLE
	STA $E000
.ENDM


.MACRO MMC3_IRQ_ENABLE
	STA $E001
.ENDM



.MACRO MMC3_SET_IRQ_VALUE[val] irq_value
	LDA #irq_value
	STA $C000
.ENDM

.MACRO MMC3_RELOAD_IRQ_VALUE 
	STA $C001
.ENDM

_F}_MMC3_MACROS